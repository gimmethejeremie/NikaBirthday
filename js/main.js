    function fadeOutAndStopBgm(durationMs = 900, options = {}) {
      if (!bgm) return Promise.resolve();
      const { rememberForResume = false } = options;

      if (bgmFadeRafId) {
        cancelAnimationFrame(bgmFadeRafId);
        bgmFadeRafId = 0;
      }

      const initialVolume = Math.max(0, Math.min(1, Number.isFinite(bgm.volume) ? bgm.volume : 1));
      const wasAudible = !bgm.paused && !bgm.muted && initialVolume > 0;
      if (rememberForResume && wasAudible) {
        shouldResumeBgmAfterSecret = true;
        bgmResumeVolume = initialVolume;
      }

      if (bgm.paused || initialVolume === 0) {
        bgm.pause();
        bgm.muted = true;
        bgm.volume = initialVolume || 1;
        syncMuteIcons();
        return Promise.resolve();
      }

      return new Promise((resolve) => {
        const startTime = performance.now();

        const tick = (now) => {
          const elapsed = now - startTime;
          const progress = Math.min(1, elapsed / durationMs);
          bgm.volume = Math.max(0, initialVolume * (1 - progress));

          if (progress >= 1) {
            bgm.pause();
            bgm.muted = true;
            bgm.volume = initialVolume;
            syncMuteIcons();
            bgmFadeRafId = 0;
            resolve();
            return;
          }

          bgmFadeRafId = requestAnimationFrame(tick);
        };

        bgmFadeRafId = requestAnimationFrame(tick);
      });
    }

