    function rafThrottle(callback) {
      let rafId = 0;
      let latestArgs = null;
      return (...args) => {
        latestArgs = args;
        if (rafId) return;
        rafId = window.requestAnimationFrame(() => {
          rafId = 0;
          callback(...(latestArgs || []));
        });
      };
    }

    function debounce(callback, waitMs = 120) {
      let timerId = 0;
      return (...args) => {
        window.clearTimeout(timerId);
        timerId = window.setTimeout(() => callback(...args), waitMs);
      };
    }

