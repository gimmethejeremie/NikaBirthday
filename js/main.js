    function playCassetteClick() {
      const nowMs = performance.now();
      if (nowMs - lastCassetteClickAt < 70) return;
      lastCassetteClickAt = nowMs;

      const AudioCtx = window.AudioContext || window.webkitAudioContext;
      if (!AudioCtx) return;

      if (!cassetteClickAudioCtx) cassetteClickAudioCtx = new AudioCtx();
      const ctx = cassetteClickAudioCtx;
      if (ctx.state === 'suspended') {
        ctx.resume().catch(() => {});
      }

      const now = ctx.currentTime;
      const master = ctx.createGain();
      master.gain.setValueAtTime(0.0001, now);
      master.gain.exponentialRampToValueAtTime(0.2, now + 0.01);
      master.gain.exponentialRampToValueAtTime(0.0001, now + 0.085);

      const osc = ctx.createOscillator();
      osc.type = 'square';
      osc.frequency.setValueAtTime(610, now);
      osc.frequency.exponentialRampToValueAtTime(300, now + 0.08);

      const noiseBuffer = ctx.createBuffer(1, Math.floor(ctx.sampleRate * 0.035), ctx.sampleRate);
      const channel = noiseBuffer.getChannelData(0);
      for (let i = 0; i < channel.length; i += 1) {
        channel[i] = (Math.random() * 2 - 1) * (1 - i / channel.length);
      }

      const noise = ctx.createBufferSource();
      noise.buffer = noiseBuffer;
      const hp = ctx.createBiquadFilter();
      hp.type = 'highpass';
      hp.frequency.value = 1200;

      osc.connect(master);
      noise.connect(hp);
      hp.connect(master);
      master.connect(ctx.destination);

      osc.start(now);
      noise.start(now);
      osc.stop(now + 0.09);
      noise.stop(now + 0.045);
    }

    function formatAudioTime(value) {
      if (!Number.isFinite(value) || value < 0) return '0:00';
      const totalSeconds = Math.floor(value);
      const minutes = Math.floor(totalSeconds / 60);
      const seconds = totalSeconds % 60;
      return `${minutes}:${String(seconds).padStart(2, '0')}`;
    }

