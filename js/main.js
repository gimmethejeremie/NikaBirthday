    function typeMessage(text, target, onDone) {
      let i = 0;
      target.textContent = '';
      typingRuntime = { text, target, onDone };
      function next() {
        if (i > text.length) {
          target.textContent = text;
          typingTimerId = null;
          typingRuntime = null;
          onDone?.();
          return;
        }
        target.textContent = `${text.slice(0, i)}${i < text.length ? '|' : ''}`;
        const ch = text[i] || '';
        i += 1;
        let delay = 38;
        if (ch === '\n') delay = 300;
        if (ch === ',' || ch === '.') delay = 120;
        typingTimerId = setTimeout(next, delay);
      }
      next();
    }

