    function playPageFlipSound() {
      try {
        const ctx = new (window.AudioContext || window.webkitAudioContext)();
        const sampleRate = ctx.sampleRate;
        // Rustling layer: short filtered noise burst
        const rustDur = 0.22;
        const rustBuf = ctx.createBuffer(1, Math.floor(sampleRate * rustDur), sampleRate);
        const rustData = rustBuf.getChannelData(0);
        for (let i = 0; i < rustData.length; i++) {
          const t = i / rustData.length;
          rustData[i] = (Math.random() * 2 - 1) * Math.pow(Math.sin(Math.PI * t), 0.6) * (1 - t * 0.7);
        }
        const rustSrc = ctx.createBufferSource();
        rustSrc.buffer = rustBuf;
        const rustFilter = ctx.createBiquadFilter();
        rustFilter.type = 'bandpass';
        rustFilter.frequency.value = 3200;
        rustFilter.Q.value = 0.9;
        const rustGain = ctx.createGain();
        rustGain.gain.setValueAtTime(0.28, ctx.currentTime);
        rustGain.gain.exponentialRampToValueAtTime(0.001, ctx.currentTime + rustDur);
        rustSrc.connect(rustFilter);
        rustFilter.connect(rustGain);
        rustGain.connect(ctx.destination);
        rustSrc.start();
        // Soft thud layer
        const thudDur = 0.12;
        const thudBuf = ctx.createBuffer(1, Math.floor(sampleRate * thudDur), sampleRate);
        const thudData = thudBuf.getChannelData(0);
        for (let i = 0; i < thudData.length; i++) {
          thudData[i] = (Math.random() * 2 - 1) * Math.exp(-i / (sampleRate * 0.018));
        }
        const thudSrc = ctx.createBufferSource();
        thudSrc.buffer = thudBuf;
        const thudFilter = ctx.createBiquadFilter();
        thudFilter.type = 'lowpass';
        thudFilter.frequency.value = 380;
        const thudGain = ctx.createGain();
        thudGain.gain.setValueAtTime(0.18, ctx.currentTime + 0.04);
        thudGain.gain.exponentialRampToValueAtTime(0.001, ctx.currentTime + thudDur + 0.04);
        thudSrc.connect(thudFilter);
        thudFilter.connect(thudGain);
        thudGain.connect(ctx.destination);
        thudSrc.start(ctx.currentTime + 0.04);
        setTimeout(() => ctx.close(), 700);
      } catch (_e) {}
    }

