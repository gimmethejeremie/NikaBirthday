    function setupWishObservers() {
      const section = document.getElementById('s-messages');
      const cards = [...document.querySelectorAll('#message-grid .fan-wishes__card')];

      wishCardObserver?.disconnect();
      wishSectionObserver?.disconnect();

      wishCardObserver = new IntersectionObserver((entries) => {
        entries.forEach((entry) => {
          if (!entry.isIntersecting) return;
          entry.target.classList.add('is-visible');
          wishCardObserver.unobserve(entry.target);
        });
      }, {
        threshold: 0.2,
        rootMargin: '0px 0px -10% 0px'
      });

      cards.forEach((card) => card.classList.remove('is-visible'));
      section.classList.remove('fan-wishes--entered');

      // Ensure reveal order: title, then grid, then card-by-card intersection reveals.
      wishSectionObserver = new IntersectionObserver((entries) => {
        entries.forEach((entry) => {
          if (!entry.isIntersecting) return;
          section.classList.add('fan-wishes--entered');
          setTimeout(() => observeWishCards(cards), 240);
          wishSectionObserver.disconnect();
        });
      }, {
        threshold: 0.25
      });

      wishSectionObserver.observe(section);
    }

