    function unlockSection(name) {
      const state = sectionState[name];
      if (!state || state.unlocked) return;
      state.unlocked = true;

      const el = getSectionEl(name);
      if (!el) return;

      el.style.display = 'flex';
      el.classList.remove('section-hidden');
      void el.offsetHeight;
      el.classList.add('section-reveal', 'section-visible');
      setTimeout(() => {
        el.scrollIntoView({ behavior: 'smooth', block: 'start' });
      }, 300);

      updateNav(name);
      startSection(name);
    }

    function completeSection(name) {
      const state = sectionState[name];
      if (!state || state.completed) return;
      state.completed = true;

      // Prevent previous section artifacts from bleeding into the next viewport.
      if (name === 'candles') {
        const toast = document.getElementById('candle-toast');
        const countdownValue = document.getElementById('countdown-value');
        if (toast) {
          toast.textContent = '';
          toast.classList.remove('show');
        }
        if (countdownValue) {
          countdownValue.textContent = '';
        }
      }

      const idx = SECTION_ORDER.indexOf(name);
      const nextName = SECTION_ORDER[idx + 1];
      if (nextName) {
        unlockSection(nextName);
      }
      highlightActiveNav();
    }

    function resetSections() {
      SECTION_ORDER.forEach((name, index) => {
        sectionState[name].unlocked = index === 0;
        sectionState[name].completed = false;
        const el = getSectionEl(name);
        if (!el) return;
        el.classList.remove('section-visible', 'section-reveal');
        if (index === 0) {
          el.style.display = 'flex';
          el.classList.add('section-visible');
          el.classList.remove('section-hidden');
        } else {
          el.style.display = 'none';
          el.classList.add('section-hidden');
        }
      });

      hideContinueButton('typing-continue-btn');
      hideContinueButton('gifts-continue-btn');
      hideContinueButton('fanart-continue-btn');
      hideContinueButton('messages-continue-btn');
      hideContinueButton('finale-btn');
      updateNav();
    }

    function highlightActiveNav() {
      const unlockedSections = SECTION_ORDER
        .filter((name) => sectionState[name].unlocked)
        .map((name) => getSectionEl(name))
        .filter(Boolean);

      let current = '';
      unlockedSections.forEach((section) => {
        const rect = section.getBoundingClientRect();
        if (rect.top <= 120) {
          current = section.id.replace('s-', '');
        }
      });

      document.querySelectorAll('.sticky-nav a[data-section]').forEach((anchor) => {
        anchor.classList.toggle('nav-active', anchor.getAttribute('data-section') === current);
      });
    }

